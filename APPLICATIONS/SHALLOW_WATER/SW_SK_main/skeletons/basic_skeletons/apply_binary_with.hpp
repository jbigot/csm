#ifndef APPLY_BINARY_WITH_H
#define APPLY_BINARY_WITH_H

namespace skelgis{

  //================================================================================
  //Types of user functions
  //-------------------------------------------------------------------------------
  struct _ApplyBinaryWith_Func
  //-------------------------------------------------------------------------------
  {
    virtual void operator()(Data *, Data *, Data *,void *) const =0;
  };
  //-------------------------------------------------------------------------------
  //================================================================================

  //================================================================================

  //-------------------------------------------------------------------------------
  struct ApplyBinaryWith
  //-------------------------------------------------------------------------------
  {
    inline static void apply(const _ApplyBinaryWith_Func& func, Data * m, Data * m2, Data * m3,void * object)
    {
      m->update();
      m2->update();
      func(m,m2,m3,object);
    }
  };
  //-------------------------------------------------------------------------------
}

#endif
